    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        getTelaAnterior().getCCurso().alterarCurso(this);
        if (this.getJTextFieldNome().getText().equals("") || this.getJTextFieldCargaHoraria().getText().equals("")) {
            this.setEnabled(true);
            this.setEnabled(true);
            
        } else {
            hide();
            this.dispose();
            getTelaAnterior().getCCurso().imprimirTodosCursosController();
            getTelaAnterior().getJTabelaCurso().changeSelection(0, getTelaAnterior().getJTabelaCurso().getColumnCount(), false, false);
            getTelaAnterior().setVisible(true);
            getTelaAnterior().setEnabled(true);
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

